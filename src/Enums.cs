namespace TiledCS
{
    /// <summary>
    /// Represents the layer type
    /// </summary>
    public enum TiledLayerType
    {
        /// <summary>
        /// Indicates that the layer is an object layer
        /// </summary>
        ObjectLayer,
        
        /// <summary>
        /// Indicates that the layer is a tile layer
        /// </summary>
        TileLayer,
        
        /// <summary>
        /// Indicates that the layer is an image layer
        /// </summary>
        ImageLayer
    }
}